package it.cs.unicam.MunicipalDigitalization.api.util;

/**
 * This enum represents the possible type of Invitation in a Contribution Contest
 */
public enum InvitationType {
    /**
     * The contribution contest will not be able to be joined by anyone
     */
    INVITE_ONLY,
    /**
     * The contribution contest will be able to be joined by anyone
     */
    PUBLIC,
}
